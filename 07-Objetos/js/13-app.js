const producto = {
    nombre: 'Monitor de 20 Pulgadas',
    precio: 300,
    disponible: true
}

console.log(Object.keys(producto)); //'Object.keys()' te muestra el nombre de las variables del objeto
console.log(Object.values(producto)); //'Object.values()' te muestra el valor de las variables del objeto
console.log(Object.entries(producto)); //'Object.entries()' te muestra el nombre de las variables del objeto y sus valores