function sumar(a, b) { //'a y b' son los parametros de la función
    console.log(a+b);
}
sumar(2, 3); //'2 y 3' son los argumentos de la función (argumentos = parametros)
sumar(200, 184);
sumar(124, 321);

function saludar(nombre, apellido) {
    console.log(`Hola ${nombre} ${apellido}`);
}
saludar('Max', 'Vilà');