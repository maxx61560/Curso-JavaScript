const puntaje = 1000;
if (puntaje == 1000) { //if = si. Tentro de '()' va la condición. Si la condición se cumple ejecutará el codigo de dentro del 'if'. '==' significa 'igual a'
    console.log('si es igual');
} else { //else = si no. No tiene condición. Si el 'if' no se cumple, se cumplirá el 'else'
    console.log('no es igual');
}

//En JavaScript no es necesario usar '{}' en el 'if' y el 'else' pero si es muy recomendable ya que en otros lenguajes si es són obligatorios
if (puntaje == 1000)
    console.log('si es igual');
else
    console.log('no es igual');