//Operadores 'mayor que' y 'menor que':

const dinero = 500;
const totalAPagar = 300;

//Mayor que = '>':
if (totalAPagar > dinero) {
    console.log('No puedes pagarlo');
} else {
    console.log('Si puedes pagarlo');
}

//Menor que = '<':
if (dinero < totalAPagar) {
    console.log('No puedes pagarlo');
} else {
    console.log('Si puedes pagarlo');
}

//Mayor o igual = '>=':
if (dinero >= totalAPagar) {
    console.log('Si puedes pagarlo');
} else {
    console.log('No puedes pagarlo');
}

