//Bucle 'do while':

let i = 1000; //Inicializamos la variable para usar en el 'do while'. Mirar ejemplos en consola y variar la variable 'i' para probar

do{ //El 'do' se ejecuta al menos una vez y luego revisa la condición del 'while'
    console.log(`Número ${i}`);
    i++; //Incremento (en este caso se pone al final del 'do')
} while(i < 100);//Condición (la cumple después de ejecutar el 'do')